#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;
	size_t			i;

	i = 0;
	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	if (!src && !dest)
		return (dest);
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return ((void *)dest);
}
